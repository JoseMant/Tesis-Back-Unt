    public function Login(Request $request){
        $usuario=Usuario::Where('username',$request->input('username'))
        ->Where('password',md5(md5($request->input('password'))))
        ->first();
        if($usuario){
            Auth::login($usuario);
            $request->session()->regenerate();
            return Auth::user();
        }else{
            return response()->json(['status' => '400', 'message' => 'Usuario incorrecto!!'], 400);
        }
        
    }

